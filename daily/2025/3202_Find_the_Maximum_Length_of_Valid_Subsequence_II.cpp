class Solution {
public:
    int maximumLength(vector<int>& nums, int k) {
        int n = nums.size();
        int ans = 0;
        vector<vector<int>> dp(k, vector<int>(k, 0));
        for (int i = 0; i < n; i++){
            int curr = nums[i] % k;
            for (int prev = 0; prev < k; prev++){
                dp[prev][curr] = dp[curr][prev] + 1;
                ans = max(ans, dp[prev][curr]);
            }
        }
        return ans;
    }
};

