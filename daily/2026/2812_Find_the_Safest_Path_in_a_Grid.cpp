class Solution {
public:
    int maximumSafenessFactor(vector<vector<int>>& grid) {
        int n = grid.size();
        auto inrange = [&](int i, int j){
            return i >= 0 && i < n && j >= 0 && j < n;
        };
        vector<int> d = {0, 1, 0, -1, 0};
        vector<vector<int>> md(n, vector<int>(n, -1));
        queue<pair<int,int>> que;
        for (int i = 0; i < n; i++){
            for (int j = 0; j < n; j++){
                if (grid[i][j] == 1){
                    md[i][j] = 0;
                    que.push({i, j});
                }
            }
        }
        while(!que.empty()){
            auto [i, j] = que.front();
            que.pop();
            for (int k = 0; k < 4; k++){
                int ni = i + d[k];
                int nj = j + d[k+1];
                if (!inrange(ni, nj) || md[ni][nj] != -1) continue;
                md[ni][nj] = md[i][j] + 1;
                que.push({ni, nj});
            }
        }
        vector<vector<int>> sf(n, vector<int>(n, -1));
        sf[0][0] = md[0][0];
        priority_queue<pair<int, pair<int,int>>, vector<pair<int, pair<int,int>>>, less<pair<int, pair<int,int>>>> pq;
        pq.push({sf[0][0], {0, 0}});
        while(!pq.empty()){
            auto [s, pos] = pq.top();
            auto [x, y] = pos;
            pq.pop();
            if (x == n - 1 && y == n - 1) return sf[n-1][n-1];
            for (int k = 0; k < 4; k++){
                int nx = x + d[k];
                int ny = y + d[k+1];
                if (!inrange(nx, ny) || sf[nx][ny] != -1) continue;
                sf[nx][ny] = min(sf[x][y], md[nx][ny]);
                pq.push({sf[nx][ny], {nx, ny}});
            }
        }
        return -1;
    }
};

